     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $table = Table::make(function ($table) {
            $table->with(Task::with('user'))->paginated();
            $table->searchable(['name', 'user.email']);
            $table->column('name');
            $table->column('created_by')
                ->label('Created By')
                ->value(function ($task) {
                    return $task->user->fullname;
                });
        });

        return $table;
    }

